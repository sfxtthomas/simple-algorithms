/**
 * Create a function that takes a number as an argument, increments the number by +1 and returns the result.
 *
 * Examples
 * addition(0) ➞ 1
 *
 * addition(9) ➞ 10
 *
 * addition(-3) ➞ -2*/

public class Challenge3 {
    public static int addition(int num) {
        num+=1;
        return num;
    }
}